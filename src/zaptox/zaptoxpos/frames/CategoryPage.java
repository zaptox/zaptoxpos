    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if (!(this.jTableCategory.getSelectedRow() == -1)) {
            int user_type_id = user_type_list.get(this.jTableCategory.getSelectedRow()).getUser_type_id();
            if (dbManager.deleteUserType(new UserType(user_type_id, "", 0)) != 0) {
                showInTable();
                this.jButtonDelete.setVisible(false);
                this.jButtonSave.setVisible(true);
                this.jButtonUpdate.setVisible(false);
                this.jTextFieldCategory.setText("enter category...");
                this.jTextFieldCategory.setForeground(Color.gray);

                JOptionPane.showMessageDialog(this, "Deleted succesfully");

            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

