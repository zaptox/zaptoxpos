    public void showInTable() {
        tableModelUserType.setRowCount(0);

        user_type_list = dbManager.getAllUsersType();
        for (UserType userType : user_type_list) {
            Vector V = new Vector();
            V.add(userType.getUser_type());
            tableModelUserType.addRow(V);
        }
    }

