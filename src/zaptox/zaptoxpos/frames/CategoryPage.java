    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed

        if (!(this.jTextFieldCategory.getText().equalsIgnoreCase("enter type...") || this.jTextFieldCategory.equals(""))) {
            if (this.jTextFieldCategory.getText().matches("[a-zA-Z]+")) {
                if (dbManager.insertUserType(new UserType(0, this.jTextFieldCategory.getText(), 1)) != 0) {
                    JOptionPane.showMessageDialog(this, "Added succesfully");
                    this.jTextFieldCategory.setForeground(Color.gray);

                    showInTable();
                    this.jTextFieldCategory.setText("enter category...");
                }
            } else {
                new PlayAideo().playErrorSound();
                JOptionPane.showMessageDialog(this, "Invalid category format", "Error", JOptionPane.ERROR_MESSAGE);

            }
        } else {
            new PlayAideo().playErrorSound();
            JOptionPane.showMessageDialog(this, "Please fill require field", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

